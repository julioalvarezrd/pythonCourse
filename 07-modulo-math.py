import math  # importando la libreria o modulo math

print(math.ceil(1.1))  # Nos lleva al numero entero mas cercano hacia arriba
print(math.floor(1.9999))  # Nos lleva al numero entero mas cercano hacia abajo
print(math.pow(5, 3))  # Nos permite elevar un numero a la potencia de algo
print(math.sqrt(9))  # Nos saca la raiz cuadrada

# Funciones nativas para trabajar con numeros
print(round(1.6))  # retorna el numero entero mas cercano.
print(abs(-54))  # nos devuelve el valor absoluto.
